    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String searchText = Search_TextField.getText().trim();

        if (searchText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a PR ID to search.", "Input Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DefaultTableModel searchModel = new DefaultTableModel(
            new String[]{"PR ID", "Item Code", "Supplier(s)", "Quantity", "Required Date", "Raised By", "Status"}, 0
        );

        List<String[]> data = FileReaderUtil.readFile("purchase_requisition.txt");
        boolean found = false;

        for (String[] parts : data) {
            if (parts.length == 7) {
                if (parts[0].equalsIgnoreCase(searchText)) {
                    searchModel.addRow(parts);
                    found = true;
                }
            }
        }
        if (found) {
            PR_Table.setModel(searchModel);
        } else {
            JOptionPane.showMessageDialog(this, "No matching records found for PR ID: " + searchText, "Search Result", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

