    private void loadTableData() {
        DefaultTableModel model = new DefaultTableModel(
            new String[]{"PR ID", "Item Code", "Supplier(s)", "Quantity", "Required Date", "Raised By", "Status"}, 0
        );

        List<String[]> data = FileReaderUtil.readFile("purchase_requisition.txt");

        for (String[] parts : data) {
            if (parts.length == 7) {
                if (parts[2].trim().isEmpty()) {
                    parts[2] = "No Supplier";
                }
                model.addRow(parts);
            }
        }
        PR_Table.setModel(model);
    }

