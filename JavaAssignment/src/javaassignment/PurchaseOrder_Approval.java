            if (po.getPoID().contains(searchText)) {  // Filter by PO ID
                tableModel.addRow(new Object[]{
                    po.getPoID(),
                    po.getPrID(),
                    po.getItemCode(),
                    po.getQuantity(),
                    po.getSupplierID(),
                    po.getPmID(),
                    po.getDate(),
                    po.getStatus()
                });
            }
