    private void loadItemtoTable(){
        DefaultTableModel model = (DefaultTableModel) ItemTable.getModel();
        model.setRowCount(0);
        for (Item item : itemList) {
            Object[] row = {
                item.getItemID(),
                item.getItemName(),
                item.getSupplierids().isEmpty() ? "No Supplier" : String.join(" | ", item.getSupplierids()),
                item.getPrice(),
                item.getSalesPrice(),
                item.getStock(),
            };
            model.addRow(row);
        }
    }
    
