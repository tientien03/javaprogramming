    private String generateNextPrId(){
        int max = 0;
        for(PurchaseRequisition pr : PRList){
            String id = pr.getPrId().substring(2);
            max = Math.max(max, Integer.parseInt(id));
        }
        return String.format("PR%03d", max + 1);
    }
    
    private void savePrToFile() {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("purchase_requisition.txt"))) {
            for (PurchaseRequisition pr : PRList) {
                writer.write(pr.toString());
                writer.newLine();
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return;
    }
    
    private ArrayList<Item> getLowStockItems(ArrayList<Item> itemList) {
        ArrayList<Item> lowStockItems = new ArrayList<>();
        for (Item item : itemList) {
            if (item.getStock() <= 20) {
                lowStockItems.add(item);
            }
        }
        return lowStockItems;
    }
    
    private void loadLowStockItemsToTable() {
        DefaultTableModel model = (DefaultTableModel) lockStockTable.getModel();
        model.setRowCount(0); // Clear previous rows

        ArrayList<Item> lowItems = getLowStockItems(itemList); // itemList must already be loaded
        for (Item item : lowItems) {
            Object[] row = {
                item.getItemID(),
                item.getItemName(),
                item.getStock(),
                item.getSupplier().getSupplierId()
            };
            model.addRow(row);
        }
    }
    
    private void clearInput(){
        ComboItem.setSelectedItem(0);
        txtDate.setText("");
        txtquantity.setText("");
        txtRaisedBy.setText("");
    }

    
