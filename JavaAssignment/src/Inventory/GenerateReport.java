    private String generateStockReport() {
        StringBuilder report = new StringBuilder();
        report.append("OWSB Inventory Stock Report\n");
        report.append("Generated on: ").append(new java.util.Date()).append("\n\n");

        report.append(String.format("%-10s %-15s %-20s %-8s %-7s %-15s\n", 
            "ITEM ID", "ITEM NAME", "SUPPLIER ID", "PRICE", "STOCK", "STATUS"));
        report.append("--------------------------------------------------------------------------\n");

        int low = 0, in = 0, over = 0;

        List<Supplier> supplierList = Supplier.loadSupplierFromFile("suppliers.txt");
        List<Item> itemList = Item.loadItemFromFile("item.txt", supplierList);

        for (Item item : itemList) {
            int stock = item.getStock();
            String status = (stock < 25) ? "Low Stock" : (stock <= 75) ? "In Stock" : "Overstocked";
            if (status.equals("Low Stock")) low++;
            else if (status.equals("In Stock")) in++;
            else over++;

            report.append(String.format("%-10s %-15s %-20s %-8.2f %-7d %-15s\n",
                item.getItemID(), item.getItemName(), 
                String.join(";", item.getSupplierids()), 
                item.getPrice(), stock, status));
        }

        report.append("\nSummary:\n");
        report.append("Total Items      : ").append(itemList.size()).append("\n");
        report.append("Low Stock Items  : ").append(low).append("\n");
        report.append("In Stock Items   : ").append(in).append("\n");
        report.append("Overstocked Items: ").append(over).append("\n");

        return report.toString();
    }


